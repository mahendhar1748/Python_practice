#Type casting is nothing but converting one datatype to another one
x="258"
print(x,type(x))
y=int(x)
print(y,type(y))

z=float(x)
print(z,type(z))

a=236.26
print(a,type(a))
b=int(a)
print(b,type(b))

#converting integer to bool
p=45
print(x,type(x))
q=bool(p)
print(q,type(q))
'''any non-zero value it will give true only'''





